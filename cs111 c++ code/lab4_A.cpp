#include <iostream>
using namespace std;
int main(){
   cout << 0 << " / " << 2 << " = " << 0/2 << endl;
   cout << 1 << " / " << 2 << " = " << 1/2 << endl;
   cout << 1 << " / " << 5 << " = " << 1/5 << endl; 
   cout << 5 << " / " << 2 << " = " << 5/2 << endl;
   cout << 1.0 << " / " << 5 << " = " << 1.0/5 << endl;
   cout << 5 << " / " << 2.0 << " = " << 5/2.0 << endl;
   cout << 123 << " / " << 10 << " = " << 123/10 << endl;
   cout << 123 << " % " << 10 << " = " << 123%10 << endl;
  
}

